    /**
     * 演示 DataChannel 使用
     * 设计原因：说明如何使用 DataChannel 进行任意数据交换
     * 技术目的：展示可靠和不可靠数据传输的配置
     */
    private fun demonstrateDataChannelUsage() {
        Log.d(TAG, "")
        Log.d(TAG, "========== DataChannel 使用 ==========")
        Log.d(TAG, "")
        Log.d(TAG, "DataChannel 用于 P2P 数据传输，可以传输任意数据：")
        Log.d(TAG, "")
        Log.d(TAG, "1. 创建 DataChannel（发起方）：")
        Log.d(TAG, "   const dc = pc.createDataChannel('myDataChannel', {")
        Log.d(TAG, "     ordered: true,           // 保证顺序")
        Log.d(TAG, "     maxRetransmits: 0        // 不重传（不可靠）")
        Log.d(TAG, "   })")
        Log.d(TAG, "")
        Log.d(TAG, "2. 接收 DataChannel（接收方）：")
        Log.d(TAG, "   pc.ondatachannel = (event) => {")
        Log.d(TAG, "     const dc = event.channel")
        Log.d(TAG, "     setupDataChannel(dc)")
        Log.d(TAG, "   }")
        Log.d(TAG, "")
        Log.d(TAG, "3. DataChannel 配置选项：")
        Log.d(TAG, "   {")
        Log.d(TAG, "     ordered: true,           // 保证顺序（可靠）")
        Log.d(TAG, "     maxRetransmits: 0,        // 最大重传次数（0=不可靠）")
        Log.d(TAG, "     maxPacketLifeTime: 1000,  // 包最大生命周期（毫秒）")
        Log.d(TAG, "     protocol: 'myProtocol',   // 子协议")
        Log.d(TAG, "     negotiated: false         // 是否手动协商")
        Log.d(TAG, "   }")
        Log.d(TAG, "")
        Log.d(TAG, "4. 发送数据：")
        Log.d(TAG, "   dc.send('Hello, peer!')")
        Log.d(TAG, "   dc.send(new Uint8Array([1, 2, 3]))")
        Log.d(TAG, "   dc.send(JSON.stringify({ type: 'message' }))")
        Log.d(TAG, "")
        Log.d(TAG, "5. 接收数据：")
        Log.d(TAG, "   dc.onmessage = (event) => {")
        Log.d(TAG, "     console.log('Received:', event.data)")
        Log.d(TAG, "   }")
        Log.d(TAG, "")
        Log.d(TAG, "6. DataChannel 事件：")
        Log.d(TAG, "   dc.onopen = () => console.log('DataChannel opened')")
        Log.d(TAG, "   dc.onclose = () => console.log('DataChannel closed')")
        Log.d(TAG, "   dc.onerror = (err) => console.error('Error:', err)")
        Log.d(TAG, "")
        Log.d(TAG, "7. 使用场景：")
