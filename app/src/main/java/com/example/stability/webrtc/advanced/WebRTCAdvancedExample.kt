        Log.d(TAG, "")
        Log.d(TAG, "3. 带宽估计：")
        Log.d(TAG, "   // 应用层带宽估计")
        Log.d(TAG, "   const stats = await pc.getStats()")
        Log.d(TAG, "   stats.forEach(report => {")
        Log.d(TAG, "     if (report.type === 'inbound-rtp') {")
