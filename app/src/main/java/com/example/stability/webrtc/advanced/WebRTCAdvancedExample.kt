        Log.d(TAG, "      type: 'candidate',")
        Log.d(TAG, "      candidate: event.candidate")
        Log.d(TAG, "    })")
        Log.d(TAG, "  }")
        Log.d(TAG, "}")
        Log.d(TAG, "")
        Log.d(TAG, "pc.ontrack = (event) => {")
        Log.d(TAG, "  remoteVideo.srcObject = event.streams[0]")
        Log.d(TAG, "}")
        Log.d(TAG, "")
        Log.d(TAG, "// 4. 添加本地轨道")
        Log.d(TAG, "navigator.mediaDevices.getUserMedia({ audio: true, video: true })")
        Log.d(TAG, "  .then((stream) => {")
        Log.d(TAG, "    localVideo.srcObject = stream")
        Log.d(TAG, "    stream.getTracks().forEach((track) => {")
        Log.d(TAG, "      pc.addTrack(track, stream)")
        Log.d(TAG, "    })")
        Log.d(TAG, "  })")
        Log.d(TAG, "")
        Log.d(TAG, "// 5. 发起方创建 Offer")
        Log.d(TAG, "async function createOffer() {")
        Log.d(TAG, "  const offer = await pc.createOffer()")
        Log.d(TAG, "  await pc.setLocalDescription(offer)")
        Log.d(TAG, "  sendToSignalingServer({ type: 'offer', sdp: offer.sdp })")
        Log.d(TAG, "}")
        Log.d(TAG, "")
        Log.d(TAG, "// 6. 响应方处理 Offer")
        Log.d(TAG, "async function handleOffer(offer) {")
        Log.d(TAG, "  await pc.setRemoteDescription(offer)")
        Log.d(TAG, "  const answer = await pc.createAnswer()")
        Log.d(TAG, "  await pc.setLocalDescription(answer)")
        Log.d(TAG, "  sendToSignalingServer({ type: 'answer', sdp: answer.sdp })")
        Log.d(TAG, "}")
        Log.d(TAG, "")
        Log.d(TAG, "// 7. 处理 ICE 候选")
        Log.d(TAG, "async function handleCandidate(candidate) {")
        Log.d(TAG, "  await pc.addIceCandidate(candidate)")
        Log.d(TAG, "}")
        Log.d(TAG, "")
        Log.d(TAG, "========================================")
    }
    
