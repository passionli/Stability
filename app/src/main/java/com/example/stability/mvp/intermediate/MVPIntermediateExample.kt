package com.example.stability.mvp.intermediate

import android.util.Log

/**
 * MVP 中级登录示例
 * 本示例演示一个完整的 MVP 登录流程实现
 */
class MVPIntermediateExample {
    
    companion object {
        private const val TAG = "MVP"
    }
    
    /**
     * 启动中级示例
     * 设计原因：提供一个入口点来演示完整的登录功能
     * 技术目的：初始化并运行 MVP 登录示例
     */
    fun start() {
        Log.d(TAG, "=== MVP 中级登录示例启动 ===")
        
        // 1. 登录流程概述
        demonstrateLoginFlow()
        
        // 2. 完整代码实现
        demonstrateCompleteImplementation()
        
        // 3. 契约类设计
        demonstrateContractDesign()
        
        // 4. Presenter 生命周期管理
        demonstrateLifecycleManagement()
        
        Log.d(TAG, "=== MVP 中级登录示例完成 ===")
    }
    
    /**
     * 演示登录流程概述
     * 设计原因：说明登录功能的业务逻辑
     * 技术目的：帮助理解登录 MVP 的实现要点
     */
    private fun demonstrateLoginFlow() {
        Log.d(TAG, "")
        Log.d(TAG, "========== 登录流程概述 ==========")
        Log.d(TAG, "")
        Log.d(TAG, "MVP 登录流程：")
        Log.d(TAG, "")
        Log.d(TAG, "1. 用户输入用户名和密码")
        Log.d(TAG, "2. 点击登录按钮")
        Log.d(TAG, "3. View 调用 Presenter.login()")
        Log.d(TAG, "4. Presenter 验证输入")
        Log.d(TAG, "   - 用户名不能为空")
        Log.d(TAG, "   - 密码不能为空")
        Log.d(TAG, "   - 密码长度 >= 6")
        Log.d(TAG, "5. Presenter 调用 Model.login()")
        Log.d(TAG, "6. Model 执行网络请求")
        Log.d(TAG, "7. Model 返回结果给 Presenter")
        Log.d(TAG, "8. Presenter 更新 View")
        Log.d(TAG, "   - 成功：跳转主页")
        Log.d(TAG, "   - 失败：显示错误信息")
        Log.d(TAG, "")
        Log.d(TAG, "MVP 登录要点：")
        Log.d(TAG, "  1. View 不处理任何业务逻辑")
        Log.d(TAG, "  2. Presenter 处理输入验证")
        Log.d(TAG, "  3. Model 封装网络请求")
        Log.d(TAG, "  4. 使用契约接口连接 Presenter 和 View")
        Log.d(TAG, "  5. 处理好生命周期，防止内存泄漏")
        Log.d(TAG, "")
        Log.d(TAG, "===================================")
    }
    
    /**
     * 演示完整代码实现
     * 设计原因：提供可运行的登录 MVP 完整代码
     * 技术目的：展示最佳实践的 MVP 实现方式
     */
    private fun demonstrateCompleteImplementation() {
        Log.d(TAG, "")
        Log.d(TAG, "========== 完整代码实现 ==========")
        Log.d(TAG, "")
        Log.d(TAG, "// 1. 创建契约接口（Contract）")
        Log.d(TAG, "interface LoginContract {")
        Log.d(TAG, "    interface View {")
        Log.d(TAG, "        fun showLoading()")
        Log.d(TAG, "        fun hideLoading()")
        Log.d(TAG, "        fun showUsernameError(message: String)")
        Log.d(TAG, "        fun showPasswordError(message: String)")
        Log.d(TAG, "        fun showLoginSuccess()")
        Log.d(TAG, "        fun showLoginError(message: String)")
        Log.d(TAG, "        fun navigateToHome()")
        Log.d(TAG, "    }")
        Log.d(TAG, "")
        Log.d(TAG, "    interface Presenter {")
        Log.d(TAG, "        fun attachView(view: View)")
        Log.d(TAG, "        fun detachView()")
        Log.d(TAG, "        fun login(username: String, password: String)")
        Log.d(TAG, "        fun checkSession()")
        Log.d(TAG, "    }")
        Log.d(TAG, "")
        Log.d(TAG, "    interface Model {")
        Log.d(TAG, "        fun login(username: String, password: String, callback: Callback)")
        Log.d(TAG, "        fun saveSession(token: String)")
        Log.d(TAG, "        fun getSession(): String?")
        Log.d(TAG, "        fun clearSession()")
        Log.d(TAG, "    }")
        Log.d(TAG, "}")
        Log.d(TAG, "")
        Log.d(TAG, "// 2. 实现 Model")
        Log.d(TAG, "class LoginModel : LoginContract.Model {")
        Log.d(TAG, "    private val prefs = MyApplication.prefs")
        Log.d(TAG, "    private val API = RetrofitClient.api")
        Log.d(TAG, "")
        Log.d(TAG, "    override fun login(username: String, password: String, callback: Callback) {")
        Log.d(TAG, "        // 使用 Retrofit 执行网络请求")
        Log.d(TAG, "        API.login(LoginRequest(username, password))")
        Log.d(TAG, "            .enqueue(object : Callback<LoginResponse> {")
        Log.d(TAG, "                override fun onResponse(call: Call<LoginResponse>, response: Response<LoginResponse>) {")
        Log.d(TAG, "                    if (response.isSuccessful) {")
        Log.d(TAG, "                        response.body()?.let {")
        Log.d(TAG, "                            saveSession(it.token)")
        Log.d(TAG, "                            callback.onSuccess(it)")
        Log.d(TAG, "                        } ?: callback.onError(\"响应数据为空\")")
        Log.d(TAG, "                    } else {")
        Log.d(TAG, "                        callback.onError(\"登录失败: \${response.code()}\")")
        Log.d(TAG, "                    }")
        Log.d(TAG, "                }")
        Log.d(TAG, "")
        Log.d(TAG, "                override fun onFailure(call: Call<LoginResponse>, t: Throwable) {")
        Log.d(TAG, "                    callback.onError(\"网络请求失败: \${t.message}\")")
        Log.d(TAG, "                }")
        Log.d(TAG, "            })")
        Log.d(TAG, "    }")
        Log.d(TAG, "")
        Log.d(TAG, "    override fun saveSession(token: String) {")
        Log.d(TAG, "        prefs.edit().putString(\"token\", token).apply()")
        Log.d(TAG, "    }")
        Log.d(TAG, "")
        Log.d(TAG, "    override fun getSession(): String? {")
        Log.d(TAG, "        return prefs.getString(\"token\", null)")
        Log.d(TAG, "    }")
        Log.d(TAG, "")
        Log.d(TAG, "    override fun clearSession() {")
        Log.d(TAG, "        prefs.edit().clear().apply()")
        Log.d(TAG, "    }")
        Log.d(TAG, "}")
        Log.d(TAG, "")
        Log.d(TAG, "// 3. 实现 Presenter")
        Log.d(TAG, "class LoginPresenter(val model: LoginContract.Model) : LoginContract.Presenter {")
        Log.d(TAG, "    private var view: LoginContract.View? = null")
        Log.d(TAG, "    private val disposables = CompositeDisposable()")
        Log.d(TAG, "")
        Log.d(TAG, "    override fun attachView(view: LoginContract.View) {")
        Log.d(TAG, "        this.view = view")
        Log.d(TAG, "    }")
        Log.d(TAG, "")
        Log.d(TAG, "    override fun detachView() {")
        Log.d(TAG, "        this.view = null")
        Log.d(TAG, "        disposables.clear()")
        Log.d(TAG, "    }")
        Log.d(TAG, "")
        Log.d(TAG, "    override fun login(username: String, password: String) {")
        Log.d(TAG, "        // 验证用户名")
        Log.d(TAG, "        if (username.isEmpty()) {")
        Log.d(TAG, "            view?.showUsernameError(\"用户名不能为空\")")
        Log.d(TAG, "            return")
        Log.d(TAG, "        }")
        Log.d(TAG, "        if (username.length < 3) {")
        Log.d(TAG, "            view?.showUsernameError(\"用户名至少3个字符\")")
        Log.d(TAG, "            return")
        Log.d(TAG, "        }")
        Log.d(TAG, "        view?.showUsernameError(\"\") // 清除错误")
        Log.d(TAG, "")
        Log.d(TAG, "        // 验证密码")
        Log.d(TAG, "        if (password.isEmpty()) {")
        Log.d(TAG, "            view?.showPasswordError(\"密码不能为空\")")
        Log.d(TAG, "            return")
        Log.d(TAG, "        }")
        Log.d(TAG, "        if (password.length < 6) {")
        Log.d(TAG, "            view?.showPasswordError(\"密码至少6个字符\")")
        Log.d(TAG, "            return")
        Log.d(TAG, "        }")
        Log.d(TAG, "        view?.showPasswordError(\"\") // 清除错误")
        Log.d(TAG, "")
        Log.d(TAG, "        // 执行登录")
        Log.d(TAG, "        view?.showLoading()")
        Log.d(TAG, "        model.login(username, password, object : Callback {")
        Log.d(TAG, "            override fun onSuccess(data: Any) {")
        Log.d(TAG, "                view?.hideLoading()")
        Log.d(TAG, "                view?.showLoginSuccess()")
        Log.d(TAG, "                view?.navigateToHome()")
        Log.d(TAG, "            }")
        Log.d(TAG, "")
        Log.d(TAG, "            override fun onError(message: String) {")
        Log.d(TAG, "                view?.hideLoading()")
        Log.d(TAG, "                view?.showLoginError(message)")
        Log.d(TAG, "            }")
        Log.d(TAG, "        })")
        Log.d(TAG, "    }")
        Log.d(TAG, "")
        Log.d(TAG, "    override fun checkSession() {")
        Log.d(TAG, "        val token = model.getSession()")
        Log.d(TAG, "        if (token != null) {")
        Log.d(TAG, "            // 自动登录")
        Log.d(TAG, "            view?.navigateToHome()")
        Log.d(TAG, "        }")
        Log.d(TAG, "    }")
        Log.d(TAG, "}")
        Log.d(TAG, "")
        Log.d(TAG, "// 4. Activity 实现 View")
        Log.d(TAG, "class LoginActivity : AppCompatActivity(), LoginContract.View {")
        Log.d(TAG, "    private lateinit var presenter: LoginContract.Presenter")
        Log.d(TAG, "    private lateinit var binding: ActivityLoginBinding")
        Log.d(TAG, "")
        Log.d(TAG, "    override fun onCreate(savedInstanceState: Bundle?) {")
        Log.d(TAG, "        super.onCreate(savedInstanceState)")
        Log.d(TAG, "        binding = ActivityLoginBinding.inflate(layoutInflater)")
        Log.d(TAG, "        setContentView(binding.root)")
        Log.d(TAG, "")
        Log.d(TAG, "        // 创建 Presenter")
        Log.d(TAG, "        presenter = LoginPresenter(LoginModel())")
        Log.d(TAG, "        presenter.attachView(this)")
        Log.d(TAG, "")
        Log.d(TAG, "        // 检查会话")
        Log.d(TAG, "        presenter.checkSession()")
        Log.d(TAG, "")
        Log.d(TAG, "        // 设置点击事件")
        Log.d(TAG, "        binding.btnLogin.setOnClickListener {")
        Log.d(TAG, "            val username = binding.etUsername.text.toString()")
        Log.d(TAG, "            val password = binding.etPassword.text.toString()")
        Log.d(TAG, "            presenter.login(username, password)")
        Log.d(TAG, "        }")
        Log.d(TAG, "    }")
        Log.d(TAG, "")
        Log.d(TAG, "    override fun onDestroy() {")
        Log.d(TAG, "        presenter.detachView()")
        Log.d(TAG, "        super.onDestroy()")
        Log.d(TAG, "    }")
        Log.d(TAG, "")
        Log.d(TAG, "    // View 接口实现...")
        Log.d(TAG, "    override fun showLoading() { binding.progressBar.visibility = View.VISIBLE }")
        Log.d(TAG, "    override fun hideLoading() { binding.progressBar.visibility = View.GONE }")
        Log.d(TAG, "    override fun showUsernameError(msg: String) { binding.tilUsername.error = msg.takeIf { it.isNotEmpty() } }")
        Log.d(TAG, "    override fun showPasswordError(msg: String) { binding.tilPassword.error = msg.takeIf { it.isNotEmpty() } }")
        Log.d(TAG, "    override fun showLoginSuccess() { Toast.makeText(this, \"登录成功\", Toast.LENGTH_SHORT).show() }")
        Log.d(TAG, "    override fun showLoginError(msg: String) { Toast.makeText(this, msg, Toast.LENGTH_SHORT).show() }")
        Log.d(TAG, "    override fun navigateToHome() { startActivity(Intent(this, HomeActivity::class.java)) }")
        Log.d(TAG, "}")
        Log.d(TAG, "")
        Log.d(TAG, "====================================")
    }
    
    /**
     * 演示契约类设计
     * 设计原因：说明 MVP 中契约接口的重要性和设计方法
     * 技术目的：提供清晰的契约设计模式
     */
    private fun demonstrateContractDesign() {
        Log.d(TAG, "")
        Log.d(TAG, "========== 契约类设计 ==========")
        Log.d(TAG, "")
        Log.d(TAG, "契约类（Contract）的优点：")
        Log.d(TAG, "  1. 将 View 和 Presenter 的接口放在一起")
        Log.d(TAG, "  2. 便于查看和理解整个模块的交互")
        Log.d(TAG, "  3. 减少接口文件数量")
        Log.d(TAG, "  4. 清晰的命名空间")
        Log.d(TAG, "")
        Log.d(TAG, "契约接口设计模式：")
        Log.d(TAG, "")
        Log.d(TAG, "interface UserContract {")
        Log.d(TAG, "    // View 接口：定义 View 可以执行的操作")
        Log.d(TAG, "    interface View {")
        Log.d(TAG, "        fun showUser(user: User)")
        Log.d(TAG, "        fun showLoading()")
        Log.d(TAG, "        fun showError(message: String)")
        Log.d(TAG, "        fun showEmpty()")
        Log.d(TAG, "    }")
        Log.d(TAG, "")
        Log.d(TAG, "    // Presenter 接口：定义 Presenter 可以执行的操作")
        Log.d(TAG, "    interface Presenter {")
        Log.d(TAG, "        fun attachView(view: View)")
        Log.d(TAG, "        fun detachView()")
        Log.d(TAG, "        fun loadUser(userId: String)")
        Log.d(TAG, "        fun saveUser(user: User)")
        Log.d(TAG, "        fun deleteUser(userId: String)")
        Log.d(TAG, "    }")
        Log.d(TAG, "")
        Log.d(TAG, "    // Model 接口：定义数据操作")
        Log.d(TAG, "    interface Model {")
        Log.d(TAG, "        fun getUser(userId: String): User")
        Log.d(TAG, "        fun saveUser(user: User): Boolean")
        Log.d(TAG, "        fun deleteUser(userId: String): Boolean")
        Log.d(TAG, "    }")
        Log.d(TAG, "}")
        Log.d(TAG, "")
        Log.d(TAG, "使用原则：")
        Log.d(TAG, "  1. View 接口只包含 UI 相关方法")
        Log.d(TAG, "  2. Presenter 接口只包含业务逻辑方法")
        Log.d(TAG, "  3. 不要在 View 接口中暴露 Model")
        Log.d(TAG, "  4. Presenter 通过回调或协程与 View 通信")
        Log.d(TAG, "  5. 使用泛型减少重复代码")
        Log.d(TAG, "")
        Log.d(TAG, "=================================")
    }
    
    /**
     * 演示生命周期管理
     * 设计原因：说明如何正确处理 MVP 的生命周期
     * 技术目的：防止内存泄漏和空指针异常
     */
    private fun demonstrateLifecycleManagement() {
        Log.d(TAG, "")
        Log.d(TAG, "========== 生命周期管理 ==========")
        Log.d(TAG, "")
        Log.d(TAG, "MVP 生命周期管理的关键点：")
        Log.d(TAG, "")
        Log.d(TAG, "1. attachView() 和 detachView()")
        Log.d(TAG, "   // 在 onCreate 中调用")
        Log.d(TAG, "   override fun onCreate(savedInstanceState: Bundle?) {")
        Log.d(TAG, "       super.onCreate(savedInstanceState)")
        Log.d(TAG, "       presenter = LoginPresenter(LoginModel())")
        Log.d(TAG, "       presenter.attachView(this) // 绑定 View")
        Log.d(TAG, "   }")
        Log.d(TAG, "")
        Log.d(TAG, "   // 在 onDestroy 中调用")
        Log.d(TAG, "   override fun onDestroy() {")
        Log.d(TAG, "       presenter.detachView() // 解绑 View")
        Log.d(TAG, "       super.onDestroy()")
        Log.d(TAG, "   }")
        Log.d(TAG, "")
        Log.d(TAG, "2. View 引用检查")
        Log.d(TAG, "   // 在调用 View 方法前检查")
        Log.d(TAG, "   override fun loginSuccess() {")
        Log.d(TAG, "       if (view != null) { // 检查 View 是否存在")
        Log.d(TAG, "           view!!.showSuccess()")
        Log.d(TAG, "       }")
        Log.d(TAG, "   }")
        Log.d(TAG, "   // 或者使用 let")
        Log.d(TAG, "   view?.let { it.showSuccess() }")
        Log.d(TAG, "")
        Log.d(TAG, "3. 网络请求取消")
        Log.d(TAG, "   // 使用 RxJava 时")
        Log.d(TAG, "   class LoginPresenter {")
        Log.d(TAG, "       private val disposables = CompositeDisposable()")
        Log.d(TAG, "")
        Log.d(TAG, "       fun login(username: String, password: String) {")
        Log.d(TAG, "           api.login(username, password)")
        Log.d(TAG, "               .subscribeOn(Schedulers.io())")
        Log.d(TAG, "               .observeOn(AndroidSchedulers.mainThread())")
        Log.d(TAG, "               .addTo(disposables) // 添加到容器")
        Log.d(TAG, "       }")
        Log.d(TAG, "")
        Log.d(TAG, "       override fun detachView() {")
        Log.d(TAG, "           disposables.clear() // 取消所有请求")
        Log.d(TAG, "       }")
        Log.d(TAG, "   }")
        Log.d(TAG, "")
        Log.d(TAG, "4. 使用 Kotlin 属性委托")
        Log.d(TAG, "   // 使用 NullObject 模式")
        Log.d(TAG, "   class LoginPresenter {")
        Log.d(TAG, "       var view: LoginContract.View? = null")
        Log.d(TAG, "           set(value) {")
        Log.d(TAG, "               field = value ?: NoOpView()")
        Log.d(TAG, "           }")
        Log.d(TAG, "   }")
        Log.d(TAG, "")
        Log.d(TAG, "   // NoOpView 实现空操作")
        Log.d(TAG, "   class NoOpView : LoginContract.View {")
        Log.d(TAG, "       override fun showLoading() {}")
        Log.d(TAG, "       override fun hideLoading() {}")
        Log.d(TAG, "       // 其他方法都是空实现")
        Log.d(TAG, "   }")
        Log.d(TAG, "")
        Log.d(TAG, "5. 使用基类简化")
        Log.d(TAG, "   abstract class BasePresenter<V> {")
        Log.d(TAG, "       protected var view: V? = null")
        Log.d(TAG, "")
        Log.d(TAG, "       fun attachView(view: V) { this.view = view }")
        Log.d(TAG, "       fun detachView() { this.view = null }")
        Log.d(TAG, "   }")
        Log.d(TAG, "")
        Log.d(TAG, "   class LoginPresenter : BasePresenter<LoginContract.View>() {")
        Log.d(TAG, "       fun login() { view?.showLoading() } // 不需要 null 检查")
        Log.d(TAG, "   }")
        Log.d(TAG, "")
        Log.d(TAG, "=====================================")
    }
}
